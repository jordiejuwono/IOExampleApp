package utils

class CalcUtils {
    fun plus(a: Int, b: Int) : Int{
        return a + b
    }
    fun minus(a: Int, b: Int) : Int{
        return a - b
    }

    fun multiply(a: Int, b: Int): Int{
        return a * b
    }

    fun divided(a: Double, b: Double): Double{
        return a / b
    }
}